export const LANGS = {
  en: "English",
  hi: "हिन्दी",
  te: "తెలుగు",
  ta: "தமிழ்",
};

export function getInitialLang() {
  try {
    const s = localStorage.getItem("insurai_lang");
    if (s && LANGS[s]) return s;
  } catch (_) {}
  const n = (typeof navigator !== "undefined" && navigator.language) || "";
  if (n.startsWith("hi")) return "hi";
  if (n.startsWith("te")) return "te";
  if (n.startsWith("ta")) return "ta";
  return "en";
}

export function persistLang(lang) {
  try {
    localStorage.setItem("insurai_lang", lang);
  } catch (_) {}
}

